import { gql } from "@apollo/client";

export const QUERY_PHOTOS = gql`
  query photos($username: String) {
    photos(username: $username) {
      _id
      photoText
      photoPlace
      photoLink
      createdAt
      username
      reactionCount
      reactions {
        _id
        createdAt
        username
        reactionBody
      }
    }
  }
`;

export const QUERY_PHOTO = gql`
  query photo($id: ID!) {
    photo(_id: $id) {
      _id
      photoText
      photoPlace
      photoLink
      createdAt
      username
      reactionCount
      reactions {
        _id
        createdAt
        username
        reactionBody
      }
    }
  }
`;

export const QUERY_USER = gql`
  query user($username: String!) {
    user(username: $username) {
      _id
      username
      email
      friendCount
      friends {
        _id
        username
      }
      photos {
        _id
        photoText
        photoPlace
        photoLink
        createdAt
        reactionCount
      }
    }
  }
`;

